#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int				i;
	unsigned char	uc;

	uc = (unsigned char)c;
	i = 0;
	while (s[i])
		i++;
	i++;
	while (i-- > 0)
		if ((unsigned char)(s[i]) == uc)
			return ((char *)(s + i));
	return (NULL);
}
