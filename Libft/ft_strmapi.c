#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	i;
	size_t	len;
	char	*ret;

	if (!s)
		return (NULL);
	len = ft_strlen(s);
	ret = ft_strdup(s);
	if (!ret)
		return (NULL);
	i = -1;
	while (++i < len)
		ret[i] = f(i, ret[i]);
	return (ret);
}
