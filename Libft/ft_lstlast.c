#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*prev;
	t_list	*cur;

	prev = lst;
	cur = lst;
	while (cur)
	{
		prev = cur;
		cur = cur->next;
	}
	return (prev);
}
